package com.justfly.entities;

public class Airports {
	private String airport1;
	private String airport2;
	private String day;
	private int traveller;

	public void setAirport1(String airport1) {
		this.airport1 = airport1;
	}

	public String getAirport1() {
		return airport1;
	}

	public void setAirport2(String airport2) {
		this.airport2 = airport2;
	}

	public String getAirport2() {
		return airport2;
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public int getTraveller() {
		return traveller;
	}

	public void setTraveller(int traveller) {
		this.traveller = traveller;
	}

}
